    public static Set<String> Bron(String input) {
        HashMap<String, Set<String>> map = parseInput(input);

        Set<String> R = new HashSet<String>();

        Set<String> X = new HashSet<String>();

        Set<String> P = new HashSet<String>(map.keySet());


        maximalClique(R, P, X, map);

        return R;

    }


    //Implementing the Bron-Kerbosch algorithm
    public static void maximalClique(Set<String> R, Set<String> P, Set<String> X, HashMap<String, Set<String>> map) {
        if (P.isEmpty() && X.isEmpty()) {
            System.out.println(R);
            return;
        }

        for (String vertex : new HashSet<String>(P)) {
            Set<String> R1 = new HashSet<String>(R);
            Set<String> P1 = new HashSet<String>(P);
            Set<String> X1 = new HashSet<String>(X);

            R1.add(vertex);
            P1.retainAll(map.get(vertex));
            X1.retainAll(map.get(vertex));

            maximalClique(R1, P1, X1, map);

            P.remove(vertex);
            X.add(vertex);
        }

    }


    public static int sets(HashMap<String, Set<String>> map) {

        Set<Connected> set = new HashSet<Connected>();

        for (String name : map.keySet()) {
            Set<String> conn = map.get(name);
            for (String name1 : conn) {
                if (map.containsKey(name1)) {
                    Set<String> conn1 = map.get(name1);

                    for (String name2 : conn1) {
                        if (conn.contains(name2) && !name2.equals(name1)) {
                            Connected c = new Connected(name, name1, name2);

                            set.add(c);
                        }

                        if (map.containsKey(name2)) {
                            if (map.get(name2).contains(name)) {
                                Connected c = new Connected(name, name1, name2);

                                set.add(c);
                            }
                        }

                    }

                }
            }
        }

        int count = 0;

        for (Connected c : set) {
            char c1 = c.s1.charAt(0);

            char c2 = c.s2.charAt(0);

            char c3 = c.s3.charAt(0);

            if (c1 == 't' || c2 == 't' || c3 == 't') {
                count = count + 1;
            }
        }

        return count;

    }





    public static HashMap<String, Set<String>> parseInput(String input) {
        String[] s = input.split("\n");

        HashMap<String, Set<String>> map = new HashMap<String, Set<String>>();

        for (String pair : s) {
            String[] s1 = pair.split("-");

            if (map.containsKey(s1[0])) {
                map.get(s1[0]).add(s1[1]);
            } else {
                Set<String> set = new HashSet<String>();

                set.add(s1[1]);

                map.put(s1[0], set);
            }

            if (map.containsKey(s1[1])) {
                map.get(s1[1]).add(s1[0]);
            } else {
                Set<String> set = new HashSet<String>();

                set.add(s1[0]);
                map.put(s1[1], set);
            }
        }

        return map;
    }


    public static class Connected {

        private String s1;

        private String s2;

        private String s3;

        public Connected(String s1, String s2, String s3) {
            this.s1 = s1;
            this.s2 = s2;
            this.s3 = s3;
        }

        public ArrayList<String> getNames() {
            ArrayList<String> s = new ArrayList<String>();

            s.add(s1);
            s.add(s2);
            s.add(s3);

            return s;
        }

        @Override
        public boolean equals(Object obj) {
            try {
                Connected c = (Connected) obj;

                ArrayList<String> names = c.getNames();
                ArrayList<String> names1 = this.getNames();

                for (String name : names) {
                    if (!names1.contains(name)) {
                        return false;
                    }
                }

                return true;


            } catch (ClassCastException e) {
                return false;
            }
        }

        @Override
        public int hashCode() {
            return 31*s1.hashCode() + 31*s2.hashCode() + 31*s3.hashCode();
        }

        @Override
        public String toString() {
            return "(" + s1 + "," + s2 + "," +s3 + ")";
        }


    }


