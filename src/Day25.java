    public static int matching(String input) {
        ArrayList<Integer[][]> grids = parseInput(input);

        ArrayList<ArrayList<Integer[]>> keysLocks = getKeysLocks(grids);

        int count = 0;

        ArrayList<Integer[]> keys = keysLocks.get(0);
        ArrayList<Integer[]> locks = keysLocks.get(1);

        for (Integer[] key : keys) {
            for (Integer[] lock : locks) {
                boolean valid = true;

                for (int i = 0; i < key.length; i++) {
                    if (key[i] + lock[i] > 5) {
                        valid = false;
                        break;
                    }
                }

                if (valid) {
                    count = count + 1;
                }

            }

        }

        return count;

    }




    public static ArrayList<ArrayList<Integer[]>> getKeysLocks(ArrayList<Integer[][]> grids) {

        ArrayList<ArrayList<Integer[]>> keysLocks = new ArrayList<ArrayList<Integer[]>>();

        ArrayList<Integer[]> keys = new ArrayList<Integer[]>();

        ArrayList<Integer[]> locks = new ArrayList<Integer[]>();

        keysLocks.add(keys);
        keysLocks.add(locks);

        for (Integer[][] grid : grids) {
            Integer[] sequence = new Integer[5];
            if (grid[0][0] == 0) { //It's a key
                for (int j = 0; j < grid[0].length; j++) {
                    int count = 0;
                    for (int i = 0; i < grid.length; i++) {
                        if (grid[i][j] == 1) {
                            count = count + 1;
                        }
                    }

                    sequence[j] = count - 1;
                }

                keys.add(sequence);

            } else {
                for (int j = 0; j < grid[0].length; j++) {
                    int count = 0;
                    for (int i = 0; i < grid.length; i++) {
                        if (grid[i][j] == 1) {
                            count = count + 1;
                        }
                    }

                    sequence[j] = count - 1;
                }

                locks.add(sequence);
            }

        }

        return keysLocks;

    }


    //parses the input into a workable format
    public static ArrayList<Integer[][]> parseInput(String input) {
        String[] s = input.split("\n\n");

        ArrayList<Integer[][]> grids = new ArrayList<Integer[][]>();

        for (String grid : s) {
            String[] rows = grid.split("\n");

            Integer[][] c = new Integer[rows.length][rows[0].length()];

            for (int i = 0; i < rows.length; i++) {
                String s1 = rows[i];
                for (int j = 0; j < rows[0].length(); j++) {
                    char curr = s1.charAt(j);

                    if (curr == '.') {
                        c[i][j] = 0;
                    } else {
                        c[i][j] = 1;
                    }
                }
            }

            grids.add(c);


        }


        return grids;
    }

