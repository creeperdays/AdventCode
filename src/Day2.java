    /**
     * Checks whether one item of the level can be removed, hence making it safe.
     * @param level the input of  the level
     * @return whether the level is safe or not
     */
    public static boolean safe2(Integer[] level) {
        for (int i = 0; i < level.length; i++) {
            Integer[] l2 = new Integer[level.length - 1];

            int k = 0;
            for (int j = 0; j < level.length; j++) {
                if (j != i) {
                    l2[k] = level[j];
                    k++;
                }
            }

            if (safeLevel(l2)) {
                return true;
            }
        }
        return false;
    }

    public static boolean safeLevel(Integer[] level) {
        return isIncreasing(level) || isDecreasing(level);
    }

    /**
     * This represents part 1 of the solution
     * @param input
     * @return returns the number of safe levels
     */
    public static int safeLevels(String input) {
        ArrayList<Integer[]> levels = parseInput(input);

        int safe = 0;

        for (Integer[] level: levels) {
            if (isIncreasing(level) || isDecreasing(level)) {
                safe = safe + 1;
            }
        }

        return safe;
    }

    /**
     * Checks whether the level is all decreasing, in which the minimum difference being at least
     * 1 and maximum difference being at least 3
     * @param list the input level
     * @return a boolean value of whether the list is all decreasing or not.
     */
    public static boolean isDecreasing(Integer[] list) {
        for (int i = 0; i < list.length - 1; i++) {
            if (list[i] <= list[i+1]) {
                return false;
            } else {
                int diff = list[i] - list[i+1];

                if (diff > 3 || diff < 1) {
                    return false;
                }
            }
        }

        return true;

    }

    /**
     * Checks whether the level is all increasing, in which the minimum difference being at least
     * 1 and maximum difference being 3
     * @param list the input level
     * @return a true or false value of whether the list is increasing correctly
     */
    public static boolean isIncreasing(Integer[] list) {
        for (int i = 0; i < list.length - 1; i++) {
            if (list[i] >= list[i+1]) {
                return false;
            } else {
                int diff = list[i+1] - list[i];

                if (diff > 3 || diff < 1) {
                    return false;
                }
            }
        }

        return true;
    }

    public static ArrayList<Integer[]> parseInput(String input) {
        String[] part1 = input.split("\n");

        ArrayList<Integer[]> levels = new ArrayList<Integer[]>();

        for (String level: part1) {
            String[] nums = level.split(" ");

            Integer[] numbers = new Integer[nums.length];

            for (int i = 0; i < nums.length; i++) {
                numbers[i] = Integer.parseInt(nums[i]);
            }

            levels.add(numbers);

        }

        return levels;
    }

