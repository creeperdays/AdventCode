    public static int calculateDistance(String input) {
        int[][] arrays = toArray1(input);

        Arrays.sort(arrays[0]);

        Arrays.sort(arrays[1]);

        int sum = 0;

        for (int i = 0; i < arrays[0].length; i++) {

            int distance = Math.abs(arrays[0][i] - arrays[1][i]);

            sum = sum + distance;
        }

        return sum;

    }

    /**
     * This Function will parse both lists of numbers into an Array of integers
     * @param input The input String
     * @return the parsed integer list of numbers
     */
    public static int[][] toArray1(String input) {
        String[] list1 = input.split("\n");

        int[][] i1 = new int[2][list1.length];

        for (int i = 0; i < list1.length; i++) {
            String[] pair = list1[i].split(" {3}");

            i1[0][i] = Integer.parseInt(pair[0]);

            i1[1][i] = Integer.parseInt(pair[1]);
        }

        return i1;

    }

