    //Retains the maximum number of bananas possible
    public static long findMax(String input) {
        int[] numbers = parseInput(input);



        ArrayList<HashMap<Sequence, Integer>> all = new ArrayList<HashMap<Sequence, Integer>>();

        for (int number : numbers) {
            int[][] diffs = find(number);

            HashMap<Sequence, Integer> map = allSequences(diffs);


            all.add(map);
        }

        //All the common sequences

        Set<Sequence> common = new HashSet<Sequence>();


        for (HashMap<Sequence, Integer> map : all) {
            common.addAll(map.keySet());
        }

        System.out.println(common.size());

        long max = 0;

        for (Sequence s : common) {
           long sum = 0;

           for (HashMap<Sequence, Integer> map : all) {
               sum = sum + map.getOrDefault(s, 0);
           }

           if (sum > max) {
               max = sum;
           }
        }

        return max;
    }


    //Returns all the sequences and their maximum values in a Hashmap
    public static HashMap<Sequence, Integer> allSequences(int[][] diffs) {

        HashMap<Sequence, Integer> map = new HashMap<Sequence, Integer>();

        for (int i = 0; i + 3 < diffs[0].length; i++) {
            int num1 = diffs[1][i];
            int num2 = diffs[1][i+1];
            int num3 = diffs[1][i+2];
            int num4 = diffs[1][i+3];

            int amount = diffs[0][i+3];

            Sequence sequence = new Sequence(num1, num2, num3, num4);

            //Only puts the first instance of that sequence
            if (!map.containsKey(sequence)) {
                map.put(sequence, amount);
            }
        }

        return map;

    }

    //returns an Array of all the differences and the respective values they occur at
    public static int[][] find(long number) {

        int[][] sequence1 = new int[2][2000];


        long num1 = number;

        String s = String.valueOf(num1);

        int prev = Integer.parseInt(String.valueOf(s.charAt(s.length() - 1)));

        for (int i = 0; i < 2000; i++) {
            num1 = calculate(num1);

            String s1 = String.valueOf(num1);

            int curr = Integer.parseInt(String.valueOf(s1.charAt(s1.length() - 1)));

            int diff = curr - prev;

            sequence1[0][i] = curr;

            sequence1[1][i] = diff;

            prev = curr;
        }


        return sequence1;
    }





    public static long sum(String input) {

        int[] numbers = parseInput(input);

        long sum = 0;

        for (int number : numbers) {
            long p = finalSecret(number);

            sum = sum + p;
        }

        return sum;

    }

    //Calculates the 2000th secret number
    public static long finalSecret(long number) {

        long num1 = number;

        for (int i = 1; i <= 2000; i++) {
            num1 = calculate(num1);
        }

        return num1;
    }

    //Calculating the first iteration of the secret number
    public static long calculate(long number) {
        long result1 = 64L *number;

        number = result1^number;

        number = number % 16777216;

        long result2 = number/32;

        number = result2^number;

        number = number % 16777216;

        long result3 = number*2048;

        number = result3^number;

        number = number % 16777216;


        return number;

    }

    public static int[] parseInput(String input) {
        String[] s = input.split("\n");

        int[] numbers = new int[s.length];

        for (int i = 0; i < s.length; i++) {
            numbers[i] = Integer.parseInt(s[i]);
        }

        return numbers;
    }

    public static class Sequence {
        private int x1;

        private int x2;

        private int x3;

        private int x4;

        public Sequence(int x1, int x2, int x3, int x4) {
            this.x1 = x1;
            this.x2 = x2;
            this.x3 = x3;
            this.x4 = x4;
        }

        public int[] getSequence() {
            int[] s = new int[4];
            s[0] = x1;
            s[1] = x2;
            s[2] = x3;
            s[3] = x4;

            return s;
        }

        @Override
        public boolean equals(Object obj) {
            try {
                Sequence s = (Sequence) obj;

                return this.x1 == s.x1 && this.x2 == s.x2 && this.x3 == s.x3 && this.x4 == s.x4;
            } catch (ClassCastException e) {
                return false;
            }
        }

        @Override
        public int hashCode() {
            int[] s = this.getSequence();

            int result = 0;

            for (int num : s) {
                result = 31*result + num;
            }

            return result;
        }


        @Override
        public String toString() {
            return "(" + x1 + "," + x2 + "," + x3 + "," + x4 + ")";
        }
    }

