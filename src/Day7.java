    public static boolean valid(long testValue, ArrayList<Integer> values) {
        if (testValue == 0 && values.isEmpty()) {
            return true;
        }

        if (testValue == 0) {
            return false;
        }

        if (values.isEmpty()) {
            return false;
        }


        if (testValue % values.getLast() == 0) {
            int last = values.getLast();
            ArrayList<Integer> values1 = new ArrayList<Integer>(values);
            values1.removeLast();
            if (valid(testValue / last, values1)) {
                return true;
            }
        }

        if (testValue >= values.getLast()) {
            int last = values.getLast();
            ArrayList<Integer> values1 = new ArrayList<Integer>(values);
            values1.removeLast();
            return valid(testValue - last, values1);
        }

        return false;
    }

    public static ArrayList<ArrayList<Integer>> parseValues(String input){
        String[] s = input.split("\n");

        ArrayList<ArrayList<Integer>> values = new ArrayList<ArrayList<Integer>>();

        for (String s1 : s) {
            String[] ss = s1.split(":");

            String[] numStrings = ss[1].split(" ");

            ArrayList<Integer> v = new ArrayList<Integer>();

            for (String number : numStrings) {
                if (!number.isEmpty()) {
                    int i = Integer.parseInt(number);
                    v.add(i);
                }
            }

            values.add(v);
        }

        return values;
    }

    public static long[] parseTestValues(String input) {
        String[] s = input.split("\n");

        long[] testValues = new long[s.length];

        for (int i = 0; i < s.length; i++) {
            String s1 = s[i];

            String[] ss = s1.split(":");

            long test = Long.parseLong(ss[0]);

            testValues[i] = test;
        }


        return testValues;
    }

