    public static long total2(String input) {
        ArrayList<ArrayList<Integer>> values = parseValues(input);

        long[] testValues = parseTestValues(input);

        long total = 0;

        for (int i = 0; i < testValues.length; i++) {

            if (valid2(testValues[i], values.get(i))) {
                total = total + testValues[i];
            }
        }

        return total;
    }

    public static boolean valid2(long testValue, ArrayList<Integer> values) {
        if (testValue == 0 && values.isEmpty()) {
            return true;
        }

        if (testValue == 0) {
            return false;
        }

        if (values.isEmpty()) {
            return false;
        }

        //Can the last operator be *
        if (testValue % values.getLast() == 0) {
            int last = values.getLast();
            ArrayList<Integer> values1 = new ArrayList<Integer>(values);
            values1.removeLast();
            if (valid2(testValue / last, values1)) {
                return true;
            }
        }

        //Can the last operator be ||
        int last = values.getLast();
        long size = (long) Math.pow(10, String.valueOf(last).length());

        if (testValue % size == last) {
            ArrayList<Integer> values1 = new ArrayList<Integer>(values);
            values1.removeLast();
            long test1 = Math.floorDiv(testValue, size);
            if (valid2(test1, values1)) {
                return true;
            }
        }

        //Can the last be +

        if (testValue >= values.getLast()) {
            int last1 = values.getLast();
            ArrayList<Integer> values1 = new ArrayList<Integer>(values);
            values1.removeLast();
            return valid2(testValue - last1, values1);
        }

        return false;
    }

